ORG 1000H
MSJ DB "INGRESE UN NUMERO:"
FIN1 DB ?
MSH DB "Caracter no valido"
FIN2 DB ?

ORG 1500H
NUM DB ?

ORG 3000H
ES_NUM: MOV DH, [BX]
SUB DH,30H
JS DEVOL1
CMP DH, 9H
JS DEVOL2
JNZ DEVOL1
DEVOL2: MOV DL, 0FFH
JMP FIN
DEVOL1: MOV DL, 00H
FIN: RET

ORG 2000H
MOV BX, OFFSET MSJ
MOV AL, OFFSET FIN1-OFFSET MSJ
INT 7
MOV BX, OFFSET NUM
INT 6
CALL ES_NUM
CMP DL, 0FFH
JNZ FALSE
MOV AL, 1
INT 7
JMP FINAL
FALSE: MOV BX, OFFSET MSH
MOV AL, OFFSET FIN2 - OFFSET MSH
INT 7
MOV CL, NUM
FINAL: INT 0
END

